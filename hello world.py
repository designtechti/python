# Estruturas condicionais #

x = 2
y = 3

if x > y:
	print("x é maior que y")

if x < y:
	print("x é menor que y")

if x == y:
	print("x é igual à y")

if x != y:
	print("x é maior à y")

if x >= y:
	print("x é maior ou igual à y")

if x <= y:
	print("x é menor ou igual à y")

